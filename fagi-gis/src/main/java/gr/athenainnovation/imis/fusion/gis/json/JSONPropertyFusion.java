package gr.athenainnovation.imis.fusion.gis.json;

/**
 *
 * @author Nick Vitsas
 */
public class JSONPropertyFusion {

    String valA;
    String pre;
    String preL;
    String valB;
    String action;

    public JSONPropertyFusion() {
    }

    public String getValA() {
        return valA;
    }

    public void setValA(String valA) {
        this.valA = valA;
    }

    public String getPre() {
        return pre;
    }

    public void setPre(String pre) {
        this.pre = pre;
    }

    public String getPreL() {
        return preL;
    }

    public void setPreL(String preL) {
        this.preL = preL;
    }

    public String getValB() {
        return valB;
    }

    public void setValB(String valB) {
        this.valB = valB;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

}
    

